/**
 * 尝试把带有“市”或粗略的地名归一化，如“上海市” -> “上海”
 */
export function getCityCode(cityName: string): string | null {
    let normalized = cityName.replace(/市$/, '');
    return cityCodes[normalized] || null;
}
